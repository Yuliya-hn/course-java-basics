package com.rakovets.course.javabasics.practice.lambdaexpressions.tv;

public class TV {
    private final String manufacturer;
    private final String modelName;
    private final String  yearOfIssue;
    private final String  diagonal;
    private final String  price;

    public TV(String manufacturer, String modelName, String yearOfIssue, String diagonal, String price) {
        this.manufacturer = manufacturer;
        this.modelName = modelName;
        this.yearOfIssue = yearOfIssue;
        this.diagonal = diagonal;
        this.price = price;
    }

}
